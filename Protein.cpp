#include "Protein.h"

Protein::Protein()
{

}
////////////////////////////////////////////////////////////////////////////////////////////
Protein::Protein(string p ,Protein_Type atype = Hormon)
{
    seq = p;
    type = atype;
}
////////////////////////////////////////////////////////////////////////////////////////////
Protein::Protein(Protein& rhs )
{
    seq = rhs.seq;
    type = rhs.type;
}
////////////////////////////////////////////////////////////////////////////////////////////
void Protein::print()
{
    cout << seq <<endl;
    cout << type <<endl;
}
////////////////////////////////////////////////////////////////////////////////////////////
Protein Protein::operator+ (Protein rhs)   // operator overloading for +
{
    if(type == rhs.type)
    {
        string s = seq+rhs.seq;
        Protein d;
        d.seq = s;
        return d;
    }
    else{cout <<"the type is not the same"<<endl;}
}
////////////////////////////////////////////////////////////////////////////////////////////
bool Protein::operator== (Protein rhs)  // operator overloading for ==
{
if(seq == rhs.seq && type == rhs.type)
        return 1;
    else
        return 0;
}
////////////////////////////////////////////////////////////////////////////////////////////
bool Protein::operator!= (Protein rhs)  // operator overloading for !=
{
    if(seq != rhs.seq || type != rhs.type)
        return 1;
    else
        return 0;
}
